package if_statements;

public class SwitchCaseExample01 {
    public static void main(String[] args) {


        int num=15;
        switch(num){
            case 5:
                System.out.println("Bu Beştir.");
                break;
            case 10:
                System.out.println("Bu Ondur.");
                break;
            case 15:
                System.out.println("Bu Onbeştir.");
                break;
            case 20:
                System.out.println("Bu Yirmidir.");
                break;
            default:
                System.out.println("Bu Bişey Değildir Ha");
        }





    }
}
