package encapsulation;

public class Person {
    private String name;
    private int age;

    // Getter metotları
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    //Setter metotları
    public void setName(String name) {
        this.name = name;
    }
    public void setAge(int age) {
        this.age = age;
    }
}
